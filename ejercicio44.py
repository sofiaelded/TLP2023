nombre1=input("Ingrese nombre: ")
nombre2=input("Ingrese nombre: ")
if nombre1 < nombre2:
    print(nombre1 + " es menor alfabéticamente que " + nombre2)
elif nombre1 > nombre2:
    print(nombre1 + " es mayor alfabéticamente que " + nombre2)
else:
    print("Los nombres son iguales alfabéticamente")
