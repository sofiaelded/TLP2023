num1=int(input("Ingrese un valor"))
num2=int(input("Ingrese otro valor"))
if num1>num2:
    suma=num1+num2
    resta=num1-num2
    print ("La suma es igual a: ")
    print(suma)
    print ("La resta es igual a: ")
    print(resta)
else:
    division=num1/num1
    multiplicacion=num1*num2
    print ("La división es igual a: ")
    print(division)
    print ("La multiplicación es igual a: ")
    print(multiplicacion)
